def build_case_evaluation_row_from_namespace(
    *,
    namespace: Mapping[str, Any],
) -> dict[str, Any]:
    return build_case_evaluation_row(
        **{key: _lookup(namespace, key) for key in _ROW_ARGUMENT_NAMES}
    )


def build_case_detail_payload_from_namespace(
    *,
    namespace: Mapping[str, Any],
) -> dict[str, Any]:
    return build_case_detail_payload(
        **{key: _lookup(namespace, key) for key in _DETAIL_ARGUMENT_NAMES}
    )


def _lookup(namespace: Mapping[str, Any], key: str) -> Any:
    return lookup_case_evaluation_value(
        namespace,
        key,
        error_prefix="case-evaluation payload input",
    )


